#수입과 교육기간 사이에 어떤 상관관계가 있는지 설명하시오.

#수입 	 121 99 41 35 40 29 35 24 50 60
#교육기간 19  20 16 16 18 12 14 12 16 17

income = c(121,99,41,35,40,29,35,24,50,60)
tr = c(19, 20, 16, 16, 18, 12, 14, 12, 16, 17)
comp <- data.frame(income,tr)
plot(income~tr, data = comp) #산점도. # '~'는 '부터 까지'가 아니라 각각 의미. '~'의 오른쪽이 x축이 됨.
cor(comp) #상관계수
#상관계수가 0.79 이상으로 높은 편에 속한다. 교육기간이 길수록 수입이 많은 양상을 띈다.


