#성적과 TV 시청시간 사이에 어떤 상관관계가 있는지 설명하시오.

#성적 	 77.5 60 50 95 55 85 72.5 80 92.5 87.5
#시청시간 14   10 20  7 25  9 15   13  4   21

grade = c(77.5, 60, 50, 95, 55, 85, 72.5, 80, 92.5, 87.5)
tv = c(14, 10, 20, 7, 25, 9, 15,  13,4,21)
tvngrade = data.frame(grade, tv)
plot(grade~tv, data = tvngrade) #산점도
cor(tvngrade) #상관계수 0.628
#상관계수는 0.628 정도로 상관관계가 있다고 볼 수 있으나 강하진 않다.

